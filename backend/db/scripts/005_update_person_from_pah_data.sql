-- Resolve matches: PAH rows by person.pah, NHS rows by person.nhs
-- NHS U### → NHSU### (remove space)
WITH matched AS (
  SELECT
    p.person_id,
    NULLIF(trim(c.csv_gender), '')    AS new_gender,
    NULLIF(trim(c.csv_nrc), '')       AS new_nrc,
    NULLIF(trim(c.csv_contact), '')   AS new_contact,
    NULLIF(trim(c.csv_occupation), '') AS new_occupation
  FROM _pah_csv c
  JOIN person p ON (
    (c.csv_id LIKE 'PAH%' AND p.pah = c.csv_id)
    OR
    (c.csv_id NOT LIKE 'PAH%' AND p.nhs =
      CASE WHEN c.csv_id LIKE 'NHS U%'
           THEN 'NHSU' || substring(c.csv_id FROM 6)
           ELSE c.csv_id
      END)
  )
  WHERE p.household_head = true OR p.fisher = true
)
UPDATE person p
SET
  gender            = COALESCE(m.new_gender,    p.gender),
  nrc               = COALESCE(m.new_nrc,       p.nrc),
  contact           = COALESCE(m.new_contact,   p.contact),
  primary_occupation = COALESCE(m.new_occupation, p.primary_occupation)
FROM matched m
WHERE p.person_id = m.person_id;

